package com.davtyan.photoeditor;


public class Position {
    private int dX,dY;

    public Position(int dX, int dY) {
        this.dX = dX;
        this.dY = dY;
    }

    public int getDX() {
        return dX;
    }

    public void setDX(int dX) {
        this.dX = dX;
    }

    public int getDY() {
        return dY;
    }

    public void setDY(int dY) {
        this.dY = dY;
    }


}
